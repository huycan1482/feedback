    {
        $subjects = Subject::where('is_active', '=', 1)->latest()->get();
        $teachers = DB::table('users')
            ->leftJoin('roles', 'roles.id', '=', 'users.role_id')
            ->where('roles.name', '=', 'teacher')
            // ->groupBy('users.id')
            ->latest('users.created_at')->get();

        return view ('admin.course.create', [
            'subjects' => $subjects,
            'teachers' => $teachers
        ]);
    }

